#include "libft.h"

int	ft_is_zero(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '+' || str[i] == '+')
		i++;
	while (str[i] && str[i] == '0')
		i++;
	if (!str[i])
		return (1);
	if (str[i] == '.')
		i++;
	while (str[i] && str[i] == '0')
		i++;
	if (i == 0)
		return (0);
	return (1);
}
